#Faça um programa em Python que receba 3 números e calcule a média aritmética entre eles.
n1 = input("Digite o primeiro número:")
n2 = input("Digite o segundo número:")
n3 = input("Digite o terceiro número:")
#Encontre a média entre os 2 números
media = int(n1) + int(n2) + int(n3) 
divisao = media / 3
#Mostre a média
print("A média entre os números é: ", divisao)